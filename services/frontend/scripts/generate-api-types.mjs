#!/usr/bin/env node
/**
 * Generates TypeScript definitions from gRPC-Gateway OpenAPI (Swagger 2) specs
 * emitted by buf under services/backend/internal/pb/proto (files named *_service.swagger.json).
 *
 * Requires: npm install (openapi-typescript v5 supports Swagger 2; v6+ requires OpenAPI 3.x)
 * Run: cd services/frontend && npm run generate:api-types
 */

import { execSync } from "node:child_process";
import { mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const frontendRoot = join(__dirname, "..");
const protoRoot = join(frontendRoot, "../backend/internal/pb/proto");
const outDir = join(frontendRoot, "types/generated");

function* walkServiceSwaggerFiles(dir) {
  for (const name of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, name.name);
    if (name.isDirectory()) {
      yield* walkServiceSwaggerFiles(p);
    } else if (name.name.endsWith("_service.swagger.json")) {
      yield p;
    }
  }
}

/** e.g. invoice -> Invoice, featureflag -> Featureflag */
function folderNameToPrefix(folder) {
  return folder.replace(/(^|[-_])(\w)/g, (_, __, c) => c.toUpperCase());
}

mkdirSync(outDir, { recursive: true });

const swaggerFiles = [...walkServiceSwaggerFiles(protoRoot)].sort();
if (swaggerFiles.length === 0) {
  console.error(`No *_service.swagger.json files under ${protoRoot}`);
  process.exit(1);
}

for (const abs of swaggerFiles) {
  const rel = relative(protoRoot, abs);
  const folder = rel.split(/[/\\]/)[0];
  const outName = `${folder}-service.d.ts`;
  const outPath = join(outDir, outName);
  execSync(
    `npx openapi-typescript "${abs}" -o "${outPath}"`,
    { cwd: frontendRoot, stdio: "inherit", shell: true },
  );
}

const indexLines = [
  "/**",
  " * Auto-generated by scripts/generate-api-types.mjs — do not edit by hand.",
  " * Run: npm run generate:api-types (after buf generate in services/backend).",
  " */",
  "",
];

for (const abs of swaggerFiles) {
  const rel = relative(protoRoot, abs);
  const folder = rel.split(/[/\\]/)[0];
  const mod = `./${folder}-service`;
  const pfx = `${folderNameToPrefix(folder)}Service`;
  indexLines.push(
    `export type { paths as ${pfx}Paths, definitions as ${pfx}Definitions, operations as ${pfx}Operations } from "${mod}";`,
  );
}

writeFileSync(join(outDir, "index.ts"), indexLines.join("\n") + "\n", "utf8");
console.log(`Wrote ${swaggerFiles.length} spec(s) and types/generated/index.ts`);
